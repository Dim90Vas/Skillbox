/* USER CODE BEGIN PV */
uint8_t tx_data[5] = {0x10, 0x20, 0x30, 0x40, 0x50};  // Master (I2C1) передаёт
uint8_t rx_data[5];                                   // Slave (I2C2) принимает
/* USER CODE END PV */

